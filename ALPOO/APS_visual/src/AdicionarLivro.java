    public AdicionarLivro(Book book) throws SQLException {
        database = new Database();
        initComponents();
        atualizarLista();
        Titulo.setText("Editar Livro");
        this.setTitle(Titulo.getText());
        
        jTextField2.setText(book.getTitle());
        jTextField5.setText(book.getISBN());
        jTextField5.setEditable(false);
        jTextField3.setText(String.valueOf(Math.round(book.getPrice() * 100.0) / 100.0));
        setDefaultCloseOperation(javax.swing.WindowConstants.HIDE_ON_CLOSE);
        int i = 0;
        for (Publisher pub:publishers) {
            if (pub.getId()==book.getPublisher_id()){
                EditoraCombo.setSelectedIndex(i);
            } else {i++;}
        }
        
        List<BookAuthor> bookAuthors = database.pesquisarLivroAutor(book.getISBN(), 0);
        int ind[] = new int[bookAuthors.size()];
        i = 0;
        
        for (BookAuthor ba: bookAuthors) {
            for (Authors author: authors) {
                if (author.getId() == ba.getAuthor_id()) {
                    ind[i] = authors.indexOf(author);
                    i++;
                }
            }
        }
        
        System.out.println(Arrays.toString(ind));
        
        jList1.setSelectedIndices(ind);
    }

