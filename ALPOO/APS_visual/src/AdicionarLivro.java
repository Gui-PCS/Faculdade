    public void atualizarLista() throws SQLException {
        authors = database.pesquisarAutor("", "", 0,false);
        
        System.out.println(authors);
        
        int i = 0;
        
        jList1.setModel(mod);
        jList1.setSelectionModel(new DefaultListSelectionModel() {
            @Override
            public void setSelectionInterval(int index0, int index1) {
                if(super.isSelectedIndex(index0)) {
                    super.removeSelectionInterval(index0, index1);
                }
                else {
                    super.addSelectionInterval(index0, index1);
                }
            }
        });
        
        for (Authors author: authors) {
            mod.addElement(author.getFname()+ " " + author.getName());
            i++;
        }
        
        publishers = database.pesquisarPublicadora("", 0, false);
        
        for (Publisher publisher: publishers) {
            EditoraCombo.addItem(publisher.getName());
        }
    }
                          
