    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            List<Authors> auth = new ArrayList<Authors>();
            jTextField2.setBackground(new java.awt.Color(255, 255, 255));
            jTextField3.setBackground(new java.awt.Color(255, 255, 255));
            jTextField5.setBackground(new java.awt.Color(255, 255, 255));
            jList1.setBackground(new java.awt.Color(255, 255, 255));
            
            if (jList1.getSelectedValuesList().size() == 0) {
                jList1.setBackground(new java.awt.Color(255, 0, 0));
                infoBox("selecione ao menos um autor");
                return;
            }
            
            for (int i: jList1.getSelectedIndices()) {
                auth.add(authors.get(i));
            }
            
            Publisher editora = publishers.get(EditoraCombo.getSelectedIndex());
            String title = jTextField2.getText();
            String isbn = jTextField5.getText();
            
            if (title.equals("")) {
                jTextField2.setBackground(new java.awt.Color(255, 0, 0));
                infoBox("Preencha o titulo");
                return;
            }
            if (isbn.equals("")) {
                jTextField5.setBackground(new java.awt.Color(255, 0, 0));
                infoBox("preencha o isbn");
                return;
            }
            
            double preco = 0.0;
            
            try {
                preco = Double.valueOf(jTextField3.getText());
            } catch (Exception ex){
                jTextField3.setBackground(new java.awt.Color(255, 0, 0));
                infoBox("preencha o preço com números");
                return;
            }
            
            Book book = new Book(title, isbn, editora.getId(), preco);
            System.out.println(jTextField5.isEditable());
            if (!jTextField5.isEditable()) {
                database.atualizarLivro(book);
                
                infoBox("Editado adicionado com sucesso");
                
                List<BookAuthor> lba = database.pesquisarLivroAutor(isbn, 0);
                
                for (BookAuthor ba: lba){
                    database.excluirLivroAutor(ba.getISBN(), ba.getAuthor_id());
                }
                
                int i = 0;
                
                for (Authors author: auth) {
                    BookAuthor bookauthor = new BookAuthor(isbn, author.getId(), i);
                    i++;
                    database.cadastrarLivroAutor(bookauthor);
                }
            } else {
                database.cadastrarLivro(book);
                
                int i = 0;
                
                for (Authors author: auth) {

                    BookAuthor bookauthor = new BookAuthor(isbn, author.getId(), i);
                    i++;
                    database.cadastrarLivroAutor(bookauthor);

                }

                infoBox("Livro adicionado com sucesso");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdicionarLivro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

