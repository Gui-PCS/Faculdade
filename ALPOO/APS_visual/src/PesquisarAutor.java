    public static boolean confirmDelete(String msg) {
        int input = JOptionPane.showConfirmDialog(null, msg, "Atenção!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        System.out.println(input);
        
        if (input == 0) {return true;} else {return false;}
    }
    
    private void atualizarLista(String name) throws SQLException {
        System.out.println(name);
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        
        if (name.contains(" ")) {
            String[] names = name.split(" ");
            authors = database.pesquisarAutor(names[0], names[1], 0, false);
        } else {
            authors = database.pesquisarAutor(name, "", 0, false);
        }
        
        dtm.setRowCount(0);
        
        for (Authors author: authors) {
            dtm.addRow(new Object[] {author.getId(), author.getFname(), author.getName()});
        }
    }
    
