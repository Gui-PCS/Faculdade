    private void botaoExcluir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluir
        try {
            String index;
            
            Authors author = authors.get(jTable1.getSelectedRow());
            
            if (confirmDelete("Você tem certeza de excluir o "+author.getFname()+" "+author.getName()+"?")) {
                
                List<BookAuthor> books_authors = database.pesquisarLivroAutor("", author.getId());

                if (books_authors.size() > 0) {if (confirmDelete("O autor possuí livros, ao exclui o autor irá excluir os livros junto")) {
                    for (BookAuthor ba : books_authors) {
                        System.out.println(ba);
                        database.excluirLivroAutor(ba.getISBN(), ba.getAuthor_id());
                        database.excluirLivro(ba.getISBN());
                    }
                } else {return;}}
            
                database.excluirAutor(author.getId());

                atualizarLista(jTextField2.getText());
            }
        } catch (SQLException ex) {
            Logger.getLogger(PesquisarAutor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botaoExcluir

